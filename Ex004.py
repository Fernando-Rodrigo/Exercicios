v = input('Digite algo: ')

print(type(v))
print('O que foi digitado é um valor decimal? ', v.isdecimal())
print('O que foi digitado é alfanumérico? ', v.isalnum())
print('O que foi digitado é alfabético? ', v.isalpha())
print('O que foi digitado é um digit? ', v.isdigit())
print('O que foi digitado é numérico?', v.isnumeric())
